CREATE TABLE PRODUCTS (
    ID               NUMBER(19, 0) GENERATED BY DEFAULT AS IDENTITY,
    CATEGORY_ID      NUMBER(19, 0)                                         NOT NULL,
    SKU              VARCHAR2(50)                                          NOT NULL,
    NAME             VARCHAR2(255),
    IMAGE_URL        VARCHAR2(500),
    SPECIFICATIONS   CLOB,
    WARRANTY_PERIOD  NUMBER(3)                   DEFAULT 12,
    HAS_IMEI         NUMBER(1)                   DEFAULT 1,
    PRICE_IMPORT_STD NUMBER(19, 2)               DEFAULT 0,
    PRICE_EXPORT_STD NUMBER(19, 2)               DEFAULT 0,
    CURRENT_STOCK    NUMBER(10)                  DEFAULT 0                 NOT NULL,
    MIN_THRESHOLD    NUMBER(10)                  DEFAULT 5,
    STATUS           VARCHAR2(20)                DEFAULT 'ACTIVE',
    IS_DELETED       NUMBER(1)                   DEFAULT 0                 NOT NULL,
    CREATED_AT       TIMESTAMP(6) WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATED_AT       TIMESTAMP(6) WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    
    CONSTRAINT PK_PRODUCTS          PRIMARY KEY (ID),
    CONSTRAINT UK_PRODUCTS_SKU      UNIQUE (SKU),
    CONSTRAINT FK_PRODUCTS_CATEGORY FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORIES (ID),
    CONSTRAINT CK_PRODUCTS_IS_DELETED CHECK (IS_DELETED IN (0, 1)),
    CONSTRAINT CK_PRODUCTS_STATUS    CHECK (STATUS IN ('ACTIVE', 'INACTIVE'))
);

CREATE TABLE WAREHOUSES (
